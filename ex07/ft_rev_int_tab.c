void	ft_rev_int_tab(int *tab, int size)
{
	int temp;
	int a;
	int b;

	a = 0;
	b = size - 1;
	while (a < b)
	{
		temp = tab[a];
		tab[b] = tab[a];
		tab[a] = temp;
		a++;
		b--;
	}
}
